#some data conversions
#specially used in inputs
x=12.386745; y=65
s='abcde'; t=(0,9,8,7,9)
print('int(x)=',int(x))  #converts to integer
print('long(x)=',int(x))    #converts to long
print('float(y)=',float(y)) #converts to float
print('complex(x,y)=',complex(x,y))  #converts to complex
print('str(y)=',str(y))  #converts to string
print('repr(x)=',repr(x))   #converts to expression string
print('chr(y)=',chr(y)) #converts to character

print('list(t)=',list(t))    #converts to list
print('tuple(s)=',tuple(s)) #converts to tuple
print('set(t)=',set(t)) #converts to set